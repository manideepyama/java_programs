class TypeCasting{
	public static void main(String args[]){
		byte b=20;
		int i=b;
		System.out.println("b val is : "+b);
		System.out.println("i val is : "+i);

		char x='A';
		int y=x;
		System.out.println("x val is : "+x);
		System.out.println("y val is : "+y);

		int n=97;
		char m=(char)n;
		System.out.println("n val is : "+n);
		System.out.println("m val is : "+m);

		double d=3.14;
		float f=(float)d;
		System.out.println("d val is : "+d);
		System.out.println("f val is : "+f);
	}
}

